package LeitorXML;



public class Prod {

    private Long cprod;
    private String xprod;
    private Integer qcom;
    private Double vuncom;
    private Double vprod;

    public Long getCprod() {
        return cprod;
    }

    public void setCprod(Long cprod) {
        this.cprod = cprod;
    }

    public String getXprod() {
        return xprod;
    }

    public void setXprod(String xprod) {
        this.xprod = xprod;
    }

    public Integer getQcom() {
        return qcom;
    }

    public void setQcom(Integer qcom) {
        this.qcom = qcom;
    }

    public Double getVuncom() {
        return vuncom;
    }

    public void setVuncom(Double vuncom) {
        this.vuncom = vuncom;
    }

    public Double getVprod() {
        return vprod;
    }

    public void setVprod(Double vprod) {
        this.vprod = vprod;
    }

    
    
}
